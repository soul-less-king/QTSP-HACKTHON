export const translations: Record<Language, Dict> = { en, ar };

export function translate(
  lang: Language,
  key: string,
  vars?: Record<string, string | number>
): string {
  let str = translations[lang]?.[key] ?? translations.en[key] ?? key;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      str = str.replace(new RegExp(`{${k}}`, "g"), String(v));
    }
  }
  return str;
}

/** Translate an attribute key (e.g. "projector" -> localized label). */
export function translateAttr(lang: Language, attrKey: string): string {
  return translate(lang, `attr_${attrKey}`);
}

/** Translate a matching explanation code into a localized phrase. */
export function translateExplanation(lang: Language, code: string): string {
  if (code.startsWith("mandatory:") || code.startsWith("important:") || code.startsWith("nice:")) {
    const attr = code.split(":")[1];
    return `${translate(lang, "exp_has")} ${translateAttr(lang, attr)}`;
  }
  return translate(lang, `exp_${code}`);
}
